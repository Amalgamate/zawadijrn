function calculateTotals(text) {
  const lines = text.trim().split('\n').slice(1); // skip header
  let totalBilled = 0;
  let totalPaid = 0;
  let totalBalance = 0;
  let count = 0;

  lines.forEach(line => {
    const parts = line.split(/\t+/);
    if (parts.length >= 4) {
      const billed = parseFloat(parts[1].replace(/,/g, ''));
      const paid = parseFloat(parts[2].replace(/,/g, ''));
      const balance = parseFloat(parts[3].replace(/,/g, ''));
      
      if (!isNaN(billed)) totalBilled += billed;
      if (!isNaN(paid)) totalPaid += paid;
      if (!isNaN(balance)) totalBalance += balance;
      count++;
    }
  });

  console.log(JSON.stringify({
    count,
    totalBilled,
    totalPaid,
    totalBalance
  }, null, 2));
}

